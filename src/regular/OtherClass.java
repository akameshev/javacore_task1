package regular;

public class OtherClass {

    /**
     * Функция для сложения чисел
     * @param a - слагаемое число
     * @param b - слагаемое число
     * @return - результат сложения
     */
    public static int add(int a, int b){
        return a+b;
    }

    /**
     * Функция для деления чисел
     * @param a - делитель
     * @param b - знаменатель
     * @return результат деления
     */
    public static int div(int a, int b){
        return a/b;
    }

    /**
     * Функция для деления по модулю
     * @param a - делитель
     * @param b - знаменатель
     * @return - результат деления по модулю
     */
    public static int mod(int a, int b){
        return a%b;
    }

    /**
     * Функция умножения
     * @param a - слагаемое
     * @param b - слагаемое
     * @return - результат сложения
     */
    public static int mul(int a, int b){
        return a*b;
    }

    /**
     * Функция вычитания
     * @param a - Число из которого вычитают
     * @param b - вычитаемое число
     * @return - результат разности чисел
     */
    public static int sub(int a, int b){
        return a-b;
    }
}
